export function numberShowType (v, s, n, nullVal) {
  if (v) {
    let str = ''
    for (let i = 0; i < n; i++) {
      str += '*'
    }
    return v.substring(0, s) + str + v.substring(s + n)
  }
  return nullVal || ''
}

export function percentage (a, b) {
  const num1 = parseFloat(a)
  const num2 = parseFloat(b)
  if (isNaN(num1) || isNaN(num2)) return '--'
  if (num2 === 0) {
    if (num1 === 0) return '0%'
    else return '--'
  }
  return ((num1 - num2) / num2 * 100).toFixed(2) + '%'
}

export function toPercent (a) {
  const num = parseFloat(a)
  if (isNaN(num)) return '--'
  return (num * 100).toFixed(2) + '%'
}
var SIGN_REGEXP = /([yMdhsm])(\1*)/g;
var DEFAULT_PATTERN = 'yyyy-MM-dd';

function padding(s, len) {
    var len = len - (s + '').length;
    for (var i = 0; i < len; i++) { s = '0' + s; }
    return s;
};
        // 格式化日期
export function format(date, pattern) {
        pattern = pattern || DEFAULT_PATTERN;
        return pattern.replace(SIGN_REGEXP, function ($0) {
            switch ($0.charAt(0)) {
                case 'y': return padding(date.getFullYear(), $0.length);
                case 'M': return padding(date.getMonth() + 1, $0.length);
                case 'd': return padding(date.getDate(), $0.length);
                case 'w': return date.getDay() + 1;
                case 'h': return padding(date.getHours(), $0.length);
                case 'm': return padding(date.getMinutes(), $0.length);
                case 's': return padding(date.getSeconds(), $0.length);
            }
        });
}
